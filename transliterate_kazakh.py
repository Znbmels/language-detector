def latin_to_cyrillic(text):
    """–ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ—Ç –ª–∞—Ç–∏–Ω—Å–∫–∏–π –∫–∞–∑–∞—Ö—Å–∫–∏–π –≤ –∫–∏—Ä–∏–ª–ª–∏—á–µ—Å–∫–∏–π"""
    
    # –°–ª–æ–≤–∞—Ä—å –æ–±—Ä–∞—Ç–Ω–æ–π —Ç—Ä–∞–Ω—Å–ª–∏—Ç–µ—Ä–∞—Ü–∏–∏
    reverse_dict = {
        'a': '–∞', 'b': '–±', 'v': '–≤', 'g': '–≥', 'd': '–¥', 'e': '–µ',
        'zh': '–∂', 'z': '–∑', 'i': '–∏', 'y': '–π', 'k': '–∫', 'q': '“õ',
        'l': '–ª', 'm': '–º', 'n': '–Ω', 'o': '–æ', 'p': '–ø', 'r': '—Ä',
        's': '—Å', 't': '—Ç', 'u': '—É', 'f': '—Ñ', 'h': '—Ö', 'ts': '—Ü',
        'ch': '—á', 'sh': '—à', 'shch': '—â', 'yo': '—ë', 'yu': '—é', 'ya': '—è',
        
        # –ó–∞–≥–ª–∞–≤–Ω—ã–µ –±—É–∫–≤—ã
        'A': '–ê', 'B': '–ë', 'V': '–í', 'G': '–ì', 'D': '–î', 'E': '–ï',
        'Zh': '–ñ', 'Z': '–ó', 'I': '–ò', 'Y': '–ô', 'K': '–ö', 'Q': '“ö',
        'L': '–õ', 'M': '–ú', 'N': '–ù', 'O': '–û', 'P': '–ü', 'R': '–†',
        'S': '–°', 'T': '–¢', 'U': '–£', 'F': '–§', 'H': '–•', 'Ts': '–¶',
        'Ch': '–ß', 'Sh': '–®', 'Shch': '–©', 'Yo': '–Å', 'Yu': '–Æ', 'Ya': '–Ø'
    }
    
    # –°–Ω–∞—á–∞–ª–∞ –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –º–Ω–æ–≥–æ—Å–∏–º–≤–æ–ª—å–Ω—ã–µ –∫–æ–º–±–∏–Ω–∞—Ü–∏–∏
    result = text
    for latin, cyrillic in reverse_dict.items():
        if len(latin) > 1:
            result = result.replace(latin, cyrillic)
    
    # –ó–∞—Ç–µ–º –æ–¥–∏–Ω–æ—á–Ω—ã–µ —Å–∏–º–≤–æ–ª—ã
    for char in result:
        if char in reverse_dict:
            result = result.replace(char, reverse_dict[char])
    
    return result

